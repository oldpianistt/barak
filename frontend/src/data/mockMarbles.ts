export interface MockApiResponse<T> {
  content: T[];
  pageable: {
    pageNumber: number;
    pageSize: number;
  };
  totalElements: number;
  totalPages: number;
  last: boolean;
}

export function getMockMarbles(page: number = 0, size: number = 20, onlyVisible: boolean = false): MockApiResponse<Marble> {
  const filtered = onlyVisible ? mockMarbles.filter(m => m.isVisible) : mockMarbles;
  const start = page * size;
  const end = start + size;
  const paginatedData = filtered.slice(start, end);

  return {
    content: paginatedData,
    pageable: {
      pageNumber: page,
      pageSize: size
    },
    totalElements: filtered.length,
    totalPages: Math.ceil(filtered.length / size),
    last: end >= filtered.length
  };
}

export function getMockMarbleById(id: number): Marble | undefined {
  return mockMarbles.find(m => m.id === id);
}
